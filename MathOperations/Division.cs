using System;

namespace MathOperations
{
    public class Division
    {

        ////Division
        public static int Div(int a, int b)
        {
            return a / b;
        }

        public static double Div(double a, double b)
        {
            double c = 0;

            c = Div(a, b);

            return c;
        }


    }
}
